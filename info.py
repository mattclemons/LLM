# Basic Python Example

# Data types
name = "LLM"  # String
age = 2  # Integer
height = 1.75  # Float
skills = ["Python", "Machine Learning"]  # List

# Function to print info
def print_info(name, age, height, skills):
    print(f"Name: {name}, Age: {age}, Height: {height}")
    print(f"Skills: {', '.join(skills)}")

# Call the function
print_info(name, age, height, skills)

